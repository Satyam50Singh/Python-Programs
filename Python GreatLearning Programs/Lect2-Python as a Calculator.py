#OPERATORS
#  +  : Addition
#  -  : Subtraction
#  *  : Multiplication
#  /  : Division 
#  %  : Modulus
#  ** : Exponent
#  // : Floor Division

print("Addition :",5+1)

print("Subtraction :",12-9)

print("Multiplication :",19*5)

print("Division :",21/6)

print("Mudolus :",28 % 3)

print("Exponent :",2**3)

print("SquareRoot :",8**1/2)

print("Floor Divison :",21//6)

#Complex Calculations

print(12+6/2-67+100*2)  # Priority  / * + -

print("Using Brackets :",((12+(6/2))-67)+(100*2))

